import Foundation

/// Набор предопределённых шаблонов локальных уведомлений приложения.
///
/// `NotificationTemplate` служит для централизованного хранения всех
/// текстов и идентификаторов, используемых при формировании уведомлений.
/// Это позволяет избегать дублирования строк и упрощает локализацию.
///
/// - Note: Каждый вложенный `enum` соответствует определённой категории уведомлений,
///   такой как избранное, корзина или самовывоз (pickup) заказа.
enum NotificationTemplate {
    
    /// Шаблон уведомления для напоминания о товарах в избранном.
    ///
    /// Используется, когда у пользователя есть сохранённые избранные товары,
    /// но ни один из них не добавлен в корзину.
    enum Favorites {
        /// Уникальный идентификатор уведомления.
        static let id = "favorites.reminder"
        
        /// Заголовок уведомления.
        static let title = "Ваши товары всё ещё здесь ❤️"
        
        /// Основной текст уведомления.
        static let body = "Загляните в избранное, возможно пора оформить заказ."
        
        /// Категория уведомления для центра уведомлений.
        static let categoryId = NotificationCategoryId.favorites
        
        /// Дополнительная информация, передаваемая вместе с уведомлением.
        ///
        /// - Note: Может использоваться для аналитики или навигации при обработке уведомления.
        static let userInfo: [AnyHashable: Any] = ["source": "favorites.reminder"]
    }
    
    /// Шаблон уведомления, напоминающего о незавершённой корзине.
    ///
    /// Используется, если пользователь добавил товары в корзину,
    /// но не завершил оформление заказа.
    enum Cart {
        /// Уникальный идентификатор уведомления.
        static let id = "cart.reminder"
        
        /// Заголовок уведомления.
        static let title = "Ваши товары ждут вас 🛍️"
        
        /// Основной текст уведомления.
        static let body = "Загляните в корзину, вдруг там ваши будущие покупки?"
        
        /// Категория уведомления для центра уведомлений.
        static let categoryId = NotificationCategoryId.cart
        
        /// Дополнительная информация, связанная с уведомлением.
        static let userInfo: [AnyHashable: Any] = ["source": "cart.reminder"]
    }
    
    /// Шаблон уведомления о готовности заказа к самовывозу (pickup) и доставке.
    enum Checkout {
        
        /// Шаблон уведомления о готовности заказа к самовывозу (pickup).
        ///
        /// Используется, когда заказ пользователя завершён и готов к выдаче.
        enum Pickup {
            /// Уникальный идентификатор уведомления.
            static let id = "checkout.pickup"
            
            /// Заголовок уведомления.
            static let title = "Ваш заказ в обработке."
            
            /// Основной текст уведомления.
            static let body = "Мы собираем ваш заказ и уведомим, когда он будет готов к самовывозу."
            
            /// Категория уведомления для центра уведомлений.
            static let categoryId = NotificationCategoryId.checkout
            
            /// Дополнительная информация, связанная с уведомлением.
            static let userInfo: [AnyHashable: Any] = ["source": "checkout.pickup"]
        }
        
        /// Шаблон уведомления о доставке заказа.
        ///
        /// Используется, когда заказ оформлен и ожидается доставка.
        enum Delivery {
            /// Уникальный идентификатор уведомления.
            static let id = "checkout.delivery"
            
            /// Заголовок уведомления.
            static let title = "Ваш заказ оформлен!"
            
            /// Основной текст уведомления.
            static let body = "Ожидайте доставку в течение 5 рабочих дней."
            
            /// Категория уведомления для центра уведомлений.
            static let categoryId = NotificationCategoryId.checkout
            
            /// Дополнительная информация, связанная с уведомлением.
            static let userInfo: [AnyHashable: Any] = ["source": "checkout.delivery"]
        }
    }
}
