import Foundation
import Combine

/// Протокол `ProfileStoreProtocol`
///
/// Определяет интерфейс для работы с данными **профиля пользователя**,
/// независимо от конкретного источника хранения (Supabase, REST API, локальный стор).
///
/// Назначение:
/// - инкапсулирует операции чтения и изменения профиля пользователя;
/// - скрывает детали реализации хранилища (БД, сеть, realtime);
/// - используется как абстракция в слое Domain / Presentation.
///
/// Основные задачи:
/// - инициализация профиля при первом входе пользователя (`ensureInitialUserProfile`);
/// - загрузка актуальных данных профиля (`fetchProfile`);
/// - обновление отдельных полей профиля (имя, e-mail, телефон);
/// - реактивное наблюдение за изменениями профиля (`listenProfile`).
///
/// Используется в:
/// - `ProfileRepository` (или напрямую в сервисах/координаторах),
///   для абстрагирования источника данных;
/// - `ProfileUserViewModel`, `EditProfileViewModel`
///   для получения и изменения данных профиля в UI.
///
/// Реализации:
/// - `SupabaseProfileStore` — хранение профиля в Supabase (PostgreSQL + Realtime);
/// - `MockProfileStore` — тестовая или preview-реализация;
/// - возможные кэшированные или комбинированные сторы.
protocol ProfileStoreProtocol: AnyObject {
    
    /// Создаёт профиль пользователя при первом входе, если он отсутствует.
    ///
    /// Если профиль уже существует, реализация может:
    /// - обновить имя и e-mail при расхождениях;
    /// - либо оставить данные без изменений (в зависимости от политики).
    ///
    /// - Parameters:
    ///   - uid: Уникальный идентификатор пользователя.
    ///   - name: Имя пользователя.
    ///   - email: Электронная почта пользователя.
    func ensureInitialUserProfile(
        uid: String,
        name: String,
        email: String
    ) async throws
    
    /// Загружает актуальные данные профиля пользователя.
    ///
    /// - Parameter uid: Уникальный идентификатор пользователя.
    /// - Returns: Модель `ProfileDTO` или `nil`, если профиль отсутствует.
    func fetchProfile(uid: String) async throws -> ProfileDTO?
    
    /// Обновляет имя пользователя.
    ///
    /// - Parameters:
    ///   - uid: Уникальный идентификатор пользователя.
    ///   - name: Новое имя пользователя.
    func updateName(uid: String, name: String) async throws
    
    /// Обновляет адрес электронной почты пользователя.
    ///
    /// - Parameters:
    ///   - uid: Уникальный идентификатор пользователя.
    ///   - email: Новый адрес электронной почты.
    func updateEmail(uid: String, email: String) async throws
    
    /// Обновляет номер телефона пользователя.
    ///
    /// - Parameters:
    ///   - uid: Уникальный идентификатор пользователя.
    ///   - phone: Новый номер телефона.
    func updatePhone(uid: String, phone: String) async throws
    
    /// Реактивно слушает изменения профиля пользователя.
    ///
    /// Используется для:
    /// - автоматического обновления UI при изменениях профиля;
    /// - синхронизации состояния между экранами;
    /// - realtime-обновлений из удалённого источника данных.
    ///
    /// - Parameter uid: Уникальный идентификатор пользователя.
    /// - Returns: Паблишер, эмитирующий актуальное состояние профиля `ProfileDTO?`.
    func listenProfile(uid: String) -> AnyPublisher<ProfileDTO?, Never>
}
